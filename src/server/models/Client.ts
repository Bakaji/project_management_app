import mongoose from "mongoose";

const clientSchema = new mongoose.Schema({
  name: {
    type: String,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    lowercase: true,
    trim: true,
  },
  phone: {
    type: String,
    required: true,
    unique: true,
    trim: true,
  },
});
const clientModel = mongoose.model("Client", clientSchema);

export { clientModel };
